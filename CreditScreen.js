/*
  CreditScreen.js

  Credit screen for the game.
*/

//*****************************************************************************


var app = app || { };
app.screens = app.screens || { };


//*****************************************************************************


app.screens[ "creditScreen" ] =
    (function()
     {
    //-------------------------------------------------------------------------

         function run( )
         {
             app.background.drawMain( );
             setInputHandler( );
         }
         
    //=========================================================================

         function stop( )
         {
             clearInputHandler( );
         }

    //=========================================================================

         function resize( )
         {
             app.background.drawMain( );
         }

    //=========================================================================
         
         function setInputHandler( )
         {
             $("#creditScreen").click(
                 function( event )
                 {
                     app.showScreen( 'aboutScreen' );
                     return false;
                 }
             );
         }
         
    //-------------------------------------------------------------------------

         function clearInputHandler( )
         {
             $("#creditScreen").off( "click" );
         }

    //=========================================================================

         return {
             run: run,
             stop: stop,
             resize: resize
         };
         
    //-------------------------------------------------------------------------
     }
)();


//*****************************************************************************
